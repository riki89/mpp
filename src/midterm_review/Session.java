package midterm_review;

public class Session {
	private String name;
	private double fee;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSessionFee() {
		return fee;
	}
	public void setSessionFee(double fee) {
		this.fee = fee;
	}
	

}
